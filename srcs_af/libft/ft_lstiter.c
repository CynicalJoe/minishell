#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (lst == NULL)
		return ;
	while (lst->next != NULL)
	{
		f(lst->content);
		if (lst->next != NULL)
			lst = lst->next;
	}
	f(lst->content);
}
